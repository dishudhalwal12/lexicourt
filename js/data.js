window.lexiCourtUtils = {
  comingSoon(event, message) {
    if (event) {
      event.preventDefault();
    }
    window.alert(message || "Coming soon");
  },
  goToDashboard(event) {
    if (event) {
      event.preventDefault();
    }
    window.location.href = "dashboard.html";
  },
  goToLogin(event) {
    if (event) {
      event.preventDefault();
    }
    window.location.href = "login.html";
  },
  statusClass(status) {
    const classes = {
      Active: "status-active",
      Pending: "status-pending",
      Closed: "status-closed",
      Won: "status-won"
    };
    return classes[status] || "status-closed";
  },
  statusBadge(status) {
    return `<span class="status-pill ${this.statusClass(status)}">${status}</span>`;
  },
  renderCaseTable(tableBodyId) {
    const body = document.getElementById(tableBodyId);
    if (!body) {
      return;
    }

    body.innerHTML = window.lexiCourtData.cases
      .map(
        (item) => `
          <tr class="clickable-row" data-href="case-detail.html">
            <td><span class="table-case-title">${item.caseId}</span></td>
            <td>
              <div class="table-case-title">${item.clientName}</div>
              <div class="metric-label">${item.caseTitle}</div>
            </td>
            <td>${item.caseType}</td>
            <td>${item.court}</td>
            <td>${this.statusBadge(item.status)}</td>
            <td>${item.filedDate}</td>
            <td>${item.nextHearing}</td>
            <td>
              <div class="action-group">
                <button class="action-chip" type="button" onclick="event.stopPropagation(); window.location.href='case-detail.html';">
                  <i class="bi bi-eye"></i>
                </button>
                <button class="action-chip" type="button" onclick="event.stopPropagation(); lexiCourtUtils.comingSoon(event, 'Edit workflow arrives in Phase 2.');">
                  <i class="bi bi-pencil-square"></i>
                </button>
              </div>
            </td>
          </tr>
        `
      )
      .join("");

    body.querySelectorAll(".clickable-row").forEach((row) => {
      row.addEventListener("click", () => {
        window.location.href = row.dataset.href;
      });
    });
  },
  renderDocuments(containerId) {
    const container = document.getElementById(containerId);
    if (!container) {
      return;
    }

    container.innerHTML = window.lexiCourtData.caseDocuments
      .map(
        (doc) => `
          <article class="doc-card">
            <div class="card-icon"><i class="bi bi-file-earmark-text"></i></div>
            <div class="doc-name">${doc.fileName}</div>
            <div class="metric-label">${doc.documentType}</div>
            <div class="doc-meta">
              <span>${doc.uploadDate}</span>
              <span>${doc.size}</span>
            </div>
          </article>
        `
      )
      .join("");
  },
  renderTimeline(containerId, events) {
    const container = document.getElementById(containerId);
    if (!container) {
      return;
    }

    container.innerHTML = events
      .map(
        (eventItem) => `
          <div class="timeline-event">
            <span class="timeline-date-pill">${eventItem.date}</span>
            <article class="timeline-card">
              <h4>${eventItem.title}</h4>
              <p class="body-copy">${eventItem.description}</p>
            </article>
          </div>
        `
      )
      .join("");
  },
  renderUserTable(tableBodyId) {
    const body = document.getElementById(tableBodyId);
    if (!body) {
      return;
    }

    body.innerHTML = window.lexiCourtData.adminUsers
      .map(
        (user) => `
          <tr>
            <td><span class="table-case-title">${user.name}</span></td>
            <td><span class="role-tag">${user.role}</span></td>
            <td>${user.cases}</td>
            <td>${this.statusBadge(user.status)}</td>
            <td>
              <div class="action-group">
                <button class="action-chip" type="button" onclick="lexiCourtUtils.comingSoon(event, 'User edit controls arrive in Phase 2.');">
                  <i class="bi bi-pencil-square"></i>
                </button>
                <button class="action-chip" type="button" onclick="lexiCourtUtils.comingSoon(event, 'Detailed audit trail arrives in Phase 2.');">
                  <i class="bi bi-three-dots"></i>
                </button>
              </div>
            </td>
          </tr>
        `
      )
      .join("");
  },
  renderAssistantMessages(containerId) {
    const container = document.getElementById(containerId);
    if (!container) {
      return;
    }

    container.innerHTML = window.lexiCourtData.assistantMessages
      .map(
        (item) => `
          <div class="chat-bubble ${item.sender}">${item.message}</div>
        `
      )
      .join("");
  },
  renderClientDocuments(containerId) {
    const container = document.getElementById(containerId);
    if (!container) {
      return;
    }

    container.innerHTML = window.lexiCourtData.clientDocuments
      .map(
        (doc) => `
          <div class="doc-row">
            <div>
              <div class="list-title">${doc.name}</div>
              <div class="metric-label">${doc.type}</div>
            </div>
            <span class="mini-pill">${doc.date}</span>
          </div>
        `
      )
      .join("");
  },
  bindCommonActions() {
    document.querySelectorAll("[data-coming-soon]").forEach((button) => {
      button.addEventListener("click", (event) => {
        const message = button.getAttribute("data-coming-soon");
        this.comingSoon(event, message);
      });
    });
  }
};

document.addEventListener("DOMContentLoaded", () => {
  window.lexiCourtUtils.bindCommonActions();
});
